    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        // TODO add your handling code here:
        try
        {
            if (this.jComboBoxDocs.getSelectedIndex()==0) 
            {
                mi_libro = new Libro(this.jTextFieldTitulo.getText(), this.jTextFieldAutor.getText(),this.jTextFieldUbicacion.getText(), Integer.parseInt(this.jTextFieldCantidad.getText()),this.jtxtReq1.getText(), Integer.parseInt(this.jtxtReq2.getText()));
                gestor.Agregar(mi_libro);
            }
            else if(this.jComboBoxDocs.getSelectedIndex() == 1)
            {
                mi_revista = new Revista(this.jTextFieldTitulo.getText(), this.jTextFieldAutor.getText(),this.jTextFieldUbicacion.getText(), Integer.parseInt(this.jTextFieldCantidad.getText()),this.jtxtReq1.getText(),this.jtxtReq2.getText(), Integer.parseInt(this.jtxtReq3.getText()));
                gestor.Agregar(mi_revista);
            }
            else if(this.jComboBoxDocs.getSelectedIndex() == 2)
            {
                mi_trabajo = new TrabajoGrado(this.jTextFieldTitulo.getText(), this.jTextFieldAutor.getText(),this.jTextFieldUbicacion.getText(), Integer.parseInt(this.jTextFieldCantidad.getText()),tipo_grado.valueOf(this.jtxtReq1.getText()),this.jtxtReq2.getText());
                gestor.Agregar(mi_trabajo);
            }
            else if(this.jComboBoxDocs.getSelectedIndex() == 3)
            {
                mi_public = new Publicacion(this.jTextFieldTitulo.getText(), this.jTextFieldAutor.getText(),this.jTextFieldUbicacion.getText(), Integer.parseInt(this.jTextFieldCantidad.getText()),this.jtxtReq1.getText());
                gestor.Agregar(mi_public);
            }

            else
            {
                // Esto no deberia pasar
            }
            JOptionPane.showMessageDialog(null, "El documento ha sido agregado", "Documento Agregado", JOptionPane.PLAIN_MESSAGE);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Verifique los datos, algunos no son correctos", "Datos no Válidos", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonAgregarActionPerformed

