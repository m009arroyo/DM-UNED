    private void Identificar() 
    {
        switch(this.seleccionado)
        {
            case "Libro": // Espacios necesarios para un libro
                    this.jLRequerido1.setText("Editorial:");
                    this.jLRequerido2.setText("Año de Publicación:");
                    this.jLRequerido2.setVisible(true);
                    this.jtxtReq2.setVisible(true);
                    this.jLRequerido3.setVisible(false);
                    this.jtxtReq3.setVisible(false);
                    
                    this.jtxtReq1.setText("Ej: PEARSON");
                    this.jtxtReq2.setText("Ej: 2008");
                break;
            case "Revista": // Espacios Necesarios para una Revista
                    this.jLRequerido1.setText("Casa de Edición:");
                    this.jLRequerido2.setText("Mes de Publicación:");
                    this.jLRequerido3.setText("Año de Publicación:");
                    this.jLRequerido2.setVisible(true);
                    this.jtxtReq2.setVisible(true);
                    this.jLRequerido3.setVisible(true);
                    this.jtxtReq3.setVisible(true);
                    this.jtxtReq1.setText("Ej: ARQ");
                    this.jtxtReq2.setText("Ej: Enero");
                    this.jtxtReq3.setText("Ej: 2013");
                break;
            case "Trabajo de Grado":
                    this.jLRequerido1.setText("Tipo de Grado:");
                    this.jLRequerido2.setText("Universidad:");
                    this.jLRequerido3.setVisible(false);
                    this.jtxtReq3.setVisible(false);
                    this.jLRequerido2.setVisible(true);
                    this.jtxtReq2.setVisible(true);
                    this.jtxtReq1.setText("D(Diplomado), B(Bachillerato), L(Licenciatura)");
                    this.jtxtReq2.setText("Ej: UNED");
                break;
            case "Publicación":
                    this.jLRequerido1.setText("Referencia al Periódico:");
                    this.jLRequerido2.setVisible(false);
                    this.jLRequerido3.setVisible(false);
                    this.jtxtReq2.setVisible(false);
                    this.jtxtReq3.setVisible(false);
                    this.jtxtReq1.setText("Ej: La Nación");
                    
                break;
            default:
                //Esto no debería pasar
        }
                        
        }
